# The underscores don't change the meaning of the numbers.
12000300
12_000_300  # They just improve their readability.


# scientigic notation
# 3e4 = 3E4 = 3 * 10000 = 30000
# 3e-4 = 3E-4 = 3 * 1/10000 = 0.0003
print(0.0000000000000000000000000003) # the output will be 3e-28


# octal numbers  !!! rarely used
# start with 0O or 0o
print(0o123) # the output will be 83


# hexadecimal numbers  !!! rarely used
# starts with 0X or 0x
print(0x123) # the output will be 291