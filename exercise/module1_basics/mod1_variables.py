# A variable is a named place in the memory where you can store data for future use.
#  variable names must start with a letter or the underscore sign.
# upper and lowercase letters are treated differently.
# some keywords can not be used as variable names. Examples: if, else, for, from, def ...

greeting = 'hello, friend!'
print(greeting)

# we can assign a new value to our variable.
greeting = 'hi, everybody!'
print(greeting)
# this will print both of the values