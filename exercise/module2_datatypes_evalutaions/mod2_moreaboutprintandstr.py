# Keyword Arguments or Named Arguments.
print('hello, world!')
print('Python speaking')
# each print and vocation adds a new line character at the end by default

# you can also change this behavior of the print function.
print('hello, world!', end='.')
print('Python speaking')
# the output will be hello, world!.Python speaking


first_name = 'John'
print('Your first name is', first_name, 'Welcome!')
# the output will be Your first name is john Welcome

# here we can specify the seperator with sep
first_name = 'John'
print('Your first name is', first_name, 'Welcome!', sep='-')
# the output will be Your first name is-john-Welcome